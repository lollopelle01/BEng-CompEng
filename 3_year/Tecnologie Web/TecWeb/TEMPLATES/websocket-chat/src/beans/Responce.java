package beans;

public class Responce {

	private String chat;
	private String message;
	
	public Responce() {
		super();
	}


	public String getChat() {
		return chat;
	}


	public void setChat(String chat) {
		this.chat = chat;
	}


	public String getMessage() {
		return message;
	}


	public void setMessage(String message) {
		this.message = message;
	}
	
	
	
	
}
