package beans;

public class Result {
	private boolean esito;
	private int somma;
	
	public Result() {
		super();
	}

	public boolean isEsito() {
		return esito;
	}

	public void setEsito(boolean esito) {
		this.esito = esito;
	}

	public int getSomma() {
		return somma;
	}

	public void setSomma(int somma) {
		this.somma = somma;
	}
	
	
	
	
}
